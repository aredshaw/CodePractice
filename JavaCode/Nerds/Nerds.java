public class Nerds {

    private int sleepHours;
    private int socialSkills;

  public Nerds(int sleepHours, int socialSkills) {
    this.sleepHours = sleepHours;
    this.socialSkills = socialSkills;
  }

  public int getSleepHours() {
    return sleepHours;
  }

  public int getSocialSkills() {
    return socialSkills;
  }
}
